# Integer (int)
age = int(input("Enter your age (integer): "))
print("Integer:", age)

# Floating-point (float)
temperature = float(input("Enter the temperature (float): "))
print("Float:", temperature)

# Boolean (bool)
is_sunny = input("Is it sunny? (yes/no): ").strip().lower() == "yes"
print("Boolean:", is_sunny)

# String (str)
greeting = input("Enter a greeting (string): ")
print("String:", greeting)

# None (represents void)
nothing = None
print("NoneType:", nothing)